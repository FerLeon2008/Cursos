//var
var helloWorld = "Hello JavaScript!"
console.log(helloWorld)

helloWorld = "Hello JavaScript! again"
console.log(helloWorld)

//let
let helloWorld2 = "Hello JavaScript 2!"
console.log(helloWorld2)

helloWorld2 = "Hello JavaScript 2! again"
console.log(helloWorld2)

//const

const helloWorld3 = "Hello JavaScript 3!"
console.log(helloWorld3)

/*  
    const no permite reasignar un valor a una variable ya definida
    var ya no se usa, debido a que genera complicaciones a la hora
    de ejecutarse, además de poderse llamar antes de ser declarada,
    lo cual es ilógico en programación
*/